package com.francescodisalesgithub.catinthebox.model;

public class QemuHardDrive
{
    private String name;
    private String size;

    public void setName(String name) {
        this.name = name;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public String getSize()
    {
        return size;
    }
}
